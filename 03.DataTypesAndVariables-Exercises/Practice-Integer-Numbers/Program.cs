using System;

namespace Practice_Integer_Numbers
{
    class Program
    {
        static void Main(string[] args)
        {
            sbyte firstEntranceNumber = sbyte.Parse(Console.ReadLine());
            byte secondEntranceNumber = byte.Parse(Console.ReadLine());
            short thirdEntranceNumber = short.Parse(Console.ReadLine());
            ushort fourthEntranceNumber = ushort.Parse(Console.ReadLine());
            uint fifthEntranceNumber = uint.Parse(Console.ReadLine());
            int sixthEntranceNumber = int.Parse(Console.ReadLine());
            long seventhEntranceNumber = long.Parse(Console.ReadLine());

            Console.WriteLine(firstEntranceNumber);
            Console.WriteLine(secondEntranceNumber);
            Console.WriteLine(thirdEntranceNumber);
            Console.WriteLine(fourthEntranceNumber);
            Console.WriteLine(fifthEntranceNumber);
            Console.WriteLine(sixthEntranceNumber);
            Console.WriteLine(seventhEntranceNumber);

        }
    }
}
